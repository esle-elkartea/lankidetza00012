     private void desabilitar()
        {
         for(int i=0;i<PanelLunes.getComponentCount();i++)
                PanelLunes.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelMartes.getComponentCount();i++)
                PanelMartes.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelMiercoles.getComponentCount();i++)
                PanelMiercoles.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelJueves.getComponentCount();i++)
                PanelJueves.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelViernes.getComponentCount();i++)
                PanelViernes.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelLunes.getComponentCount();i++)
                PanelSabado.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelLunes.getComponentCount();i++)
                PanelSabado.getComponent(i).setEnabled(false);
          for(int i=0;i<PanelDomingo.getComponentCount();i++)
                PanelDomingo.getComponent(i).setEnabled(false);
         
         checkLunes.setEnabled(false);
         checkMartes.setEnabled(false);
         checkMiercoles.setEnabled(false);
         checkJueves.setEnabled(false);
         checkViernes.setEnabled(false);
         checkSabado.setEnabled(false);
         checkDomingo.setEnabled(false);
         
        }
    
