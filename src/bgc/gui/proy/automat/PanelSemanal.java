     /**Devuelve un array con los días seleccionados*/
    public Dia[] getDiasSelec() {
	    Dia[]ad=new Dia[7];
	    
	    for(int i=0;i<7;i++) {
		ad[i]=null;
		if (((JPanel)panelesHoras.get(dias[i])).isVisible() ) {
		    Dia d=new Dia();
		    d.setDia(i);
		    d.setHora( ((Integer) ((JSpinner)((JPanel)panelesHoras.get(dias[i])).getComponent(1)).getValue()).intValue() );
		    d.setMin( ((Integer) ((JSpinner)((JPanel)panelesHoras.get(dias[i])).getComponent(3)).getValue()).intValue() );
		    ad[i]=d;
		}
	    }
	    
	    return ad;
	}
    
